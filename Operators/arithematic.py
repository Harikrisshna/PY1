# (+,-,*,/,//,%,**)

no1 = int(input("Enter the first number: "))
no2 = int(input("Enter the first number: "))

# addition
res = no1 + no2
print("add res=", res)

# subtraction
res = no1 - no2
print("sub res=", res)

# multiplication
res = no1 * no2
print("multiply res=", res)

# division
res = no1 / no2
print("div res=", res)

# floor division(excludes the decimals)
res = no1 // no2
print("floor div res=", res)

# modulus(remainder)
res = no1 % no2
print("modulus res=", res)

# exponent
res = no1 ** no2
print("exponent res=", res)
